<script>
import MenuComponent from '@/components/admin/menuComponent.vue';
import { mapMixin } from '@/utils/mapMixin.js';
import AMapLoader from '@/utils/loadAMap.js';
// 【已修改】导入所有需要的模块
import bicycleIcon from '@/components/icons/bicycle.png';
import { getMapAreaBicycles } from '@/api/map/bicycle';
import { getParkingAreasInBounds, convertParkingAreaData } from '@/api/map/parking.js';

export default {
  name: "TasksView",
  components: {
    MenuComponent
  },
  mixins: [mapMixin],
  data() {
    return {
      listCollapsed: false,
      remindDisabled: false,
      searchText: "",
      searchKey: "",
      filterStatus: "all",
      statusFilters: [
        { label: "全部", value: "all" },
        { label: "未接收", value: "pending" },
        { label: "正在处理", value: "processing" },
        { label: "已完成", value: "done" }
      ],
      tasks: [
        { id: "T20240710001", startLocation: "深圳市-福田区-福华三路-区域A", endLocation: "深圳市-南山区-科技园-区域E", workerName: "李明", workerPhone: "13800000001", deployAmount: 10, status: "pending" },
        { id: "T20240710002", startLocation: "深圳市-福田区-会展中心-区域D", endLocation: "深圳市-福田区-金田路-区域B", workerName: "王芳", workerPhone: "13800000002", deployAmount: 8, status: "processing" },
        { id: "T20240709003", startLocation: "深圳市-福田区-滨河大道-区域C", endLocation: "深圳市-福田区-福华三路-区域A", workerName: "张伟", workerPhone: "13800000003", deployAmount: 12, status: "done" },
        { id: "T20240709004", startLocation: "深圳市-南山区-科技园-区域E", endLocation: "深圳市-福田区-会展中心-区域D", workerName: "赵丽", workerPhone: "13800000004", deployAmount: 5, status: "pending" },
        { id: "T20240708005", startLocation: "深圳市-福田区-金田路-区域B", endLocation: "深圳市-福田区-滨河大道-区域C", workerName: "钱涛", workerPhone: "13800000005", deployAmount: 7, status: "processing" }
      ],
      // 【已修改】清空硬编码数据，准备接收动态API数据
      parkingAreas: [],
      bikes: [],
      showBikes: true,
      polygons: []
    };
  },
  computed: {
    filteredTasks() {
      let list = this.filterStatus === "all"
          ? this.tasks
          : this.tasks.filter(t => t.status === this.filterStatus);
      const kw = this.searchKey.trim().toLowerCase();
      if (kw) {
        list = list.filter(t =>
            t.id.toLowerCase().includes(kw) ||
            t.startLocation.toLowerCase().includes(kw) ||
            t.endLocation.toLowerCase().includes(kw) ||
            t.workerName.toLowerCase().includes(kw) ||
            (t.workerPhone && t.workerPhone.includes(kw))
        );
      }
      return list;
    }
  },
  methods: {
    // --- 地图数据加载与绘制 ---

    // 【新增】统一的数据加载入口，方便管理
    loadAllMapData() {
      this.loadBicycles();
      this.loadParkingAreas();
    },

    async loadBicycles() {
      if (!this.map) return;
      try {
        const bounds = this.map.getBounds();
        const params = { minLat: bounds.getSouthWest().lat, maxLat: bounds.getNorthEast().lat, minLng: bounds.getSouthWest().lng, maxLng: bounds.getNorthEast().lng };
        const response = await getMapAreaBicycles(params);
        const bikesForMixin = response.data.map(bike => ({ ...bike, lng: bike.currentLon, lat: bike.currentLat, id: bike.bikeId }));
        this.bikes = bikesForMixin;
        const bikeMarkerIcon = new window.AMap.Icon({ image: bicycleIcon, size: new window.AMap.Size(32, 32), imageSize: new window.AMap.Size(32, 32) });
        this.addBikeMarkers(this.bikes, bikeMarkerIcon);
        if (!this.showBikes) { this.markers.forEach(marker => marker.hide()); }
      } catch (error) { console.error("加载单车数据失败:", error); }
    },

    // 【新增】从API加载停车区域数据的方法
    async loadParkingAreas() {
      if (!this.map) return;
      try {
        const bounds = this.map.getBounds();
        const params = {
          minLat: bounds.getSouthWest().lat,
          maxLat: bounds.getNorthEast().lat,
          minLon: bounds.getSouthWest().lng,
          maxLon: bounds.getNorthEast().lng
        };
        const response = await getParkingAreasInBounds(params);
        let rawData = (response && response.data && Array.isArray(response.data)) ? response.data : null;

        if (rawData) {
          this.parkingAreas = convertParkingAreaData(rawData);
        } else {
          this.parkingAreas = [];
        }
      } catch (error) {
        console.error("加载停车区域数据失败:", error);
        this.parkingAreas = [];
      } finally {
        // 保证无论成功与否，都执行重绘
        this.drawParkingAreas();
      }
    },

    addBikeMarkers(bikeList, bikeIcon) {
      this.markers.forEach(marker => marker.setMap(null));
      this.markers = [];
      bikeList.forEach(bike => {
        const marker = new window.AMap.Marker({ position: [bike.lng, bike.lat], map: this.map, icon: bikeIcon, title: `单车编号: ${bike.id}` });
        marker.on('mouseover', () => {
          this.infoWindow.setContent(`<div style="padding: 8px 12px; font-size: 14px;"><b>单车编号：</b>${bike.id}</div>`);
          this.infoWindow.open(this.map, marker.getPosition());
        });
        marker.on('mouseout', () => this.infoWindow.close());
        this.markers.push(marker);
      });
    },

    onToggleBikes() {
      this.showBikes = !this.showBikes;
      if (this.markers && this.markers.length > 0) {
        this.markers.forEach(marker => {
          this.showBikes ? marker.show() : marker.hide();
        });
      }
    },

    // 【已修改】根据从API获取的动态数据来绘制停车区域
    drawParkingAreas() {
      const infoWindow = new window.AMap.InfoWindow({
        offset: new window.AMap.Pixel(0, -20)
      });
      this.map.remove(this.polygons);
      this.polygons = [];
      if (!Array.isArray(this.parkingAreas)) return;

      this.parkingAreas.forEach(area => {
        const polygon = new window.AMap.Polygon({
          path: area.polygonPath, // 使用转换函数生成的路径
          fillColor: "#FFD600",
          fillOpacity: 0.3,
          strokeColor: "#FFD600",
          strokeWeight: 2,
          zIndex: 50,
          cursor: "pointer"
        });
        polygon.setMap(this.map);
        polygon.on("mouseover", (e) => {
          // 使用转换函数生成的数据字段
          infoWindow.setContent(`
            <div style="min-width:160px;">
              <b>停车区域:</b> ${area.geohash}<br/>
              <b>停车容量:</b> ${area.parkingCapacity || 'N/A'}<br/>
              <b>区域组ID:</b> ${area.regionGroupId || 'N/A'}
            </div>`);
          infoWindow.open(this.map, e.lnglat);
        });
        polygon.on("mouseout", () => infoWindow.close());
        this.polygons.push(polygon);
      });
    },

    // --- 页面UI和业务逻辑方法 ---
    handleProfileSaved(formData) {
      console.log('个人资料已保存:', formData);
      window.alert('个人信息已在控制台输出。');
    },
    goHome() {
      this.$router.push("/admin");
    },
    statusText(status) {
      const map = { pending: "未接收", processing: "正在处理", done: "已完成" };
      return map[status] || "";
    },
    statusClass(status) {
      return `status-${status}`;
    },
    statusIconClass(status) {
      const map = { pending: "icon-pending", processing: "icon-processing", done: "icon-done" };
      return map[status] || "";
    },
    remindTask() {
      this.remindDisabled = true;
      alert("催促成功");
      setTimeout(() => {
        this.remindDisabled = false;
      }, 1000);
    },
    reselectTask() {
      this.$router.push("/location");
    },
    deleteTask(task) {
      if (window.confirm("确定要删除该任务吗？")) {
        this.tasks = this.tasks.filter(t => t.id !== task.id);
        alert("删除成功");
      }
    },
    onSearch() {
      this.searchKey = this.searchText;
    },
    clearSearch() {
      this.searchText = "";
      this.searchKey = "";
    },
  },
  mounted() {
    AMapLoader.load('dea7cc14dad7340b0c4e541dfa3d27b7', 'AMap.Heatmap').then(() => {
      this.initMap();
      this.map.setZoomAndCenter(17, [114.0580, 22.5390]);
      this.searchKey = "";

      // 【已修改】初始加载所有地图数据，并为地图交互绑定统一的加载函数
      this.loadAllMapData();
      this.map.on('moveend', this.loadAllMapData);
      this.map.on('zoomend', this.loadAllMapData);

    }).catch(err => {
      alert('地图加载失败: ' + err.message);
    });
  },
  beforeDestroy() {
    if (this.map) {
      // 【已修改】移除监听器，防止内存泄漏
      this.map.off('moveend', this.loadAllMapData);
      this.map.off('zoomend', this.loadAllMapData);
      this.map.destroy();
    }
  }
};
</script>

